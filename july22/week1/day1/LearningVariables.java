package july22.week1.day1;

public class LearningVariables {

	public static void main(String[] args) {
		int a = 20; //declare variable
		int b = 30; //declare variable
		int sum = a+b;
		System.out.println(sum);
		
		byte c = 10;  //declare variables
		short d = 20;
		long s = 20L;
		boolean e = true;
		float f = 10f;
		double g = 10.55;
		
		System.out.println("byte value: "+c); //printing variable value
		System.out.println("short value: "+d);
		System.out.println("long value: "+s);
		System.out.println("boolean value: "+e);
		System.out.println("float value: "+f);
		System.out.println("double value: "+g);
		
		String name = "Global ERP"; //declaring string
		System.out.println(name);
	}

}
